package com.example.matkamasthi.model;

public class DoubleDataRecyclerModel {
    String editedValueText;
    String numberText;
    String rangeText;

    public DoubleDataRecyclerModel(){}

    public String getEditedValueText() {
        return editedValueText;
    }

    public void setEditedValueText(String editedValueText) {
        this.editedValueText = editedValueText;
    }

    public String getNumberText() {
        return numberText;
    }

    public void setNumberText(String numberText) {
        this.numberText = numberText;
    }

    public String getRangeText() {
        return rangeText;
    }

    public void setRangeText(String rangeText) {
        this.rangeText = rangeText;
    }
}
